/*************** CART ***************/
function addToCart(itemId){
  const item = menuItems.find(i=>i.id===itemId);
  const existing = cart.find(i=>i.id===itemId);
  if (existing) existing.quantity++; else cart.push({...item, quantity:1});
  updateCart(); showNotification('ƒê√£ th√™m v√†o gi·ªè h√†ng!');
}
function updateCart(){
  const cartItems = document.getElementById('cartItems');
  const cartCount = document.getElementById('cartCount');
  const cartTotal = document.getElementById('cartTotal');

  cartCount.textContent = cart.reduce((s,i)=>s+i.quantity,0);

  if (cart.length===0){
    cartItems.innerHTML = `<div class="empty-cart"><p>Gi·ªè h√†ng tr·ªëng</p><p style="font-size:.9rem;margin-top:.5rem;">Th√™m m√≥n ƒÉn v√†o gi·ªè h√†ng nh√©!</p></div>`;
    cartTotal.textContent = '0ƒë'; return;
  }

  cartItems.innerHTML = cart.map(item=>`
    <div class="cart-item">
      <img src="${item.image}" alt="${item.name}">
      <div class="cart-item-info">
        <h4>${item.name}</h4>
        <p style="color:#667eea;font-weight:bold;">${item.price.toLocaleString()}ƒë</p>
        <div class="cart-item-controls">
          <button class="qty-btn" onclick="decreaseQuantity(${item.id})">-</button>
          <span style="padding:0 1rem;font-weight:bold;">${item.quantity}</span>
          <button class="qty-btn" onclick="increaseQuantity(${item.id})">+</button>
          <button class="remove-item" onclick="removeFromCart(${item.id})">X√≥a</button>
        </div>
      </div>
    </div>`).join('');

  const total = cart.reduce((s,i)=>s+i.price*i.quantity,0);
  cartTotal.textContent = total.toLocaleString()+'ƒë';
}
function increaseQuantity(id){ const it=cart.find(i=>i.id===id); if(it){it.quantity++; updateCart();} }
function decreaseQuantity(id){ const it=cart.find(i=>i.id===id); if(it && it.quantity>1){it.quantity--; updateCart();} }
function removeFromCart(id){ cart = cart.filter(i=>i.id!==id); updateCart(); }
